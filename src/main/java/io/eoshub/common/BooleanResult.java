package io.eoshub.common;

public class BooleanResult {

    private boolean result;

    public BooleanResult() {
    }

    public BooleanResult(boolean result) {
        this.result = result;
    }

    public boolean isResult() {
        return result;
    }

    public void setResult(boolean result) {
        this.result = result;
    }
}
